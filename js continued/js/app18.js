// Math object

let num = 4.5678;
console.log(Math.floor(num));
console.log(Math.ceil(num));

num = 4;
console.log(Math.sqrt(num));

num = Math.PI;
console.log(num);

num = Math.min(1, 2, 3, 5);
console.log(num);

num = Math.max(1, 2, 5, 9);
console.log(num);

/* Math.random(), Returns a pseudorandom number between 0 and 1. */
num = Math.random();
console.log(num);
num = Math.random() * 10;
console.log(num);
