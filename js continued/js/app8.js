// ternary operator

/* unary operator :
Contains only one operand. e.g.: typeof */
console.log(typeof 1);

/* binary operator :
Contains two operands. e.g.: assignment */
let num = 3;

/* ternary operator:
condition ? (runs if true) : (runs if false) */
const val = 1 > 1;
val ? console.log(`it's true`) : console.log(`it's false`);
