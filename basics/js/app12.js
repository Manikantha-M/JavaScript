// comparison operators
// >, <, >=, <=, ==, ===, !=, !==
// == checks only value.
// === checks both value and type.
// != checks only value.
// !== not equal value or not equal type.
// JavaScript has implicit type conversion.

const n1 = 6;
const n2 = '6';
const value = n1 == n2;
console.log(value);
const value2 = n1 === n2;
console.log(value2);
const value3 = n1 !== n2;
console.log(value3);
const value4 = n1 != n2;
console.log(value4);
