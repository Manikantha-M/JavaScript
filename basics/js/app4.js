// variables
// we can declare a variable using let, const, var
/* var has been there from the beginning of JavaScript.
let and const were introduced in ECMAScript 2015 aka ES6. 
Since var has some shortcomings, it is not mostly used. */
/* We cannot re-assign the variable, which was declared using const. 
For variables we may use camelCase naming convention in JavaScript. */
let a = 'Mani';
console.log(a);
let add, zip, state;
add = 'Amalapuram';
zip = '533201';
state = 'AP';
console.log(add, zip, state);
a = 'hello';
console.log(a);
var b = 'html';
const c = 'css';
console.log(b);
console.log(c);
